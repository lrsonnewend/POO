package RevisaoEstruturaRepeticao;

public class Rwhile {
    public static void main(String[] args) {
        int quantAster = 8;
        
        System.out.println("**********");
        
        while(quantAster  > 0){
            System.out.println("*        *");
            quantAster-=1;
    
        }
        
        System.out.println("**********");

    }
}
